# ~/.bashrc

# Stop loading configs if non interactive
[[ $- != *i* ]] && return;

# Source global definitions
if [ -f /etc/bashrc ]; then
	. /etc/bashrc
fi

# #############################################################################
# User configuration
# #############################################################################

# -----------------------------------------------------------------------------
# Dotdir directories
# -----------------------------------------------------------------------------
[[ -n $BSHDOTDIR ]] || export BSHDOTDIR="$HOME/.config/bash"

# -----------------------------------------------------------------------------
# Load Bash Resouce Configuration
# -----------------------------------------------------------------------------
source $BSHDOTDIR/rc.sh

# -----------------------------------------------------------------------------
# Cleanup PATH (recommended to leave this as final step)
# -----------------------------------------------------------------------------
export PATH=$(echo "$PATH" | awk -v RS=: -v ORS=: '!a[$0]++' | sed 's/:$//')

